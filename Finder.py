def finder_1(numbers, x):
    c=0
    for i in range(0,len(numbers)-1):
        if numbers[i]==x:
            c+=1
            print("Элемент \"", x, "\" расположен под индексом ", c, " в списке")
            i+=1
    print (x, " встречается в списке ", c, " раз(а)")

finder_1([2,4,5,6,4,5,7,2], 4)
finder_1(["Москва", "Симферополь", "Выборг", "Санкт-Петербург", "Москва"], "Москва")
